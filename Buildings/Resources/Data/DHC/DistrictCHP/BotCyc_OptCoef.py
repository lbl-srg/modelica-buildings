# Functions
def calculate_cv_rmse(measurements, simulations):
    measurements = np.array(measurements)
    simulations = np.array(simulations)
    rmse = np.sqrt(np.mean((measurements - simulations) ** 2))
    mean_measurements = np.mean(measurements)
    cv_rmse = (rmse / mean_measurements) * 100
    return cv_rmse
#------------------------------------------
def calculate_rmse(measurements, simulations):
    measurements = np.array(measurements)
    simulations = np.array(simulations)
    rmse = np.sqrt(np.mean((measurements - simulations) ** 2))
    return rmse
